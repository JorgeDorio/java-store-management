package q1;

public class Item {
  private String name;
  private float value;
  private String cod;
  
  public String getCod() {
    return cod;
  }
  public void setCod(String cod) {
    this.cod = cod;
  }
  public String getName() {
    return name;
  }
  public void setName(String name) {
    this.name = name;
  }
  public float getValue() {
    return value;
  }
  public void setValue(float value) {
    this.value = value;
  }
}
